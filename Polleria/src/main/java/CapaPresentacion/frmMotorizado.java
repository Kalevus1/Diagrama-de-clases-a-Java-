        char codMotorizado = txtCod.getText().charAt(0);
        String nombreMotorizado = txtNombre.getText();
        String celularMotorizado = txtNumTelf.getText();
        String atiendePedido = txtAten.getText();
        
        motorizado.setCodMotorizado(codMotorizado);
        motorizado.setNombreMotorizado(nombreMotorizado);
